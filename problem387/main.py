# How would you explain the difference between an API and SDK to a non-technical person?

# One technique we like to use when explaining technical concepts to non-technical people is to use analogies to help with memory retention.

# For this type of question, we'll use the Restaurant example:

# Imagine you're at a restaurant with a variety of waiters - each waiter will only take your order for one specific item (i.e. one waiter for pizza, one waiter for pasta, etc.).
# An API is like a waiter in this example as it will only perform a specific order. 
